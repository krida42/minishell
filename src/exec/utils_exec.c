#include "minishell.h"

void	ft_putstr(char *s)
{
	int	i;

	if (s == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}

void	ft_putchar(char c)
{
	if (write (1, &c, 1) < 0)
		exit(EXIT_FAILURE);
}
